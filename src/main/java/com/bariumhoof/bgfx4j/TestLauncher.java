    private static final FloatBuffer viewBuf = MemoryUtil.memAllocFloat(16);
    private static final Matrix4f view = new Matrix4f();
    private static final Matrix4f proj = new Matrix4f();
    private static final FloatBuffer projBuf = MemoryUtil.memAllocFloat(16);
    private static final Matrix4f model = new Matrix4f();
    private static final FloatBuffer modelBuf = MemoryUtil.memAllocFloat(16);

    public static void main(String[] args) throws IOException {
        int width  = 1280;
        int height = 720;

        if (!glfwInit()) {
            throw new RuntimeException("Error initializing GLFW");
        }

        // the client (renderer) API is managed by bgfx
        glfwWindowHint(GLFW_CLIENT_API, GLFW_NO_API);
        if (Platform.get() == Platform.MACOSX) {
            glfwWindowHint(GLFW_COCOA_RETINA_FRAMEBUFFER, GLFW_FALSE);
        }

        long window = glfwCreateWindow(width, height, "25-C99", 0, 0);
        if (window == NULL) {
            throw new RuntimeException("Error creating GLFW window");
        }

        glfwSetKeyCallback(window, (windowHnd, key, scancode, action, mods) -> {
            if (action != GLFW_RELEASE) {
                return;
            }

            switch (key) {
                case GLFW_KEY_ESCAPE:
                    glfwSetWindowShouldClose(windowHnd, true);
                    break;
            }
        });

//        ImmutableInitTest
//                .builder()
//                .resolution();

//        ImmutableInitTest.builder().resolution(ImmutableResolutionTest.builder().)
//        ImmutableResolutionTest.
//        ImmutableResolutionTest.builder().

        try (MemoryStack stack = stackPush()) {
            BGFXInit init = BGFXInit.mallocStack(stack);
            bgfx_init_ctor(init);
            init
                    .resolution(it -> it
                            .width(width)
                            .height(height)
                            .reset(BGFX_RESET_VSYNC));
//                            .reset(BGFX_RESET_NONE));

            switch (Platform.get()) {
                case LINUX:
                    init.platformData()
                            .ndt(GLFWNativeX11.glfwGetX11Display())
                            .nwh(GLFWNativeX11.glfwGetX11Window(window));
                    break;
                case MACOSX:
                    init.platformData()
                            .nwh(GLFWNativeCocoa.glfwGetCocoaWindow(window));
                    break;
                case WINDOWS:
                    init.platformData()
                            .nwh(GLFWNativeWin32.glfwGetWin32Window(window));
                    break;
            }

            if (!bgfx_init(init)) {
                throw new RuntimeException("Error initializing bgfx renderer");
            }

            zZeroToOne = !bgfx_get_caps().homogeneousDepth();
        }

        System.out.println("bgfx renderer: " + bgfx_get_renderer_name(bgfx_get_renderer_type()));

        // Enable debug text.
        bgfx_set_debug(BGFX_DEBUG_TEXT);

        bgfx_set_view_clear(0, BGFX_CLEAR_COLOR | BGFX_CLEAR_DEPTH, 0x303030ff, 1.0f, 0);

        final ByteBuffer logo = Logo.createLogo();

        // MY CODE
        final VertexDecl decl = VertexDecl.builder(BGFX_RENDERER_TYPE.METAL)
                .beginWith(BGFX_ATTRIB.POSITION, BGFX_ATTRIB_TYPE.FLOAT)
                .thenUseNormalized(BGFX_ATTRIB.COLOR0, BGFX_ATTRIB_TYPE.UINT8)
                .build();

        final Number[][] cubeVertices = {
                { -1.0f, 1.0f, 1.0f, 0xff000000 },
                { 1.0f, 1.0f, 1.0f, 0xff0000ff },
                { -1.0f, -1.0f, 1.0f, 0xff00ff00 },
                { 1.0f, -1.0f, 1.0f, 0xff00ffff },
                { -1.0f, 1.0f, -1.0f, 0xffff0000 },
                { 1.0f, 1.0f, -1.0f, 0xffff00ff },
                { -1.0f, -1.0f, -1.0f, 0xffffff00 },
                { 1.0f, -1.0f, -1.0f, 0xffffffff }
        };

        final int[] cubeIndices = {
                0, 1, 2, // 0
                1, 3, 2,
                4, 6, 5, // 2
                5, 6, 7,
                0, 2, 4, // 4
                4, 2, 6,
                1, 5, 3, // 6
                5, 7, 3,
                0, 4, 1, // 8
                4, 5, 1,
                2, 3, 6, // 10
                6, 3, 7
        };

        // these make up the model matrix:
        final VertexBuffer vertexBuffer = VertexBuffer.create(decl, cubeVertices);
        final IndexBuffer indexBuffer = IndexBuffer.create(cubeIndices);

        final Program program = Program.load(
                TestLauncher.class.getResource("/shaders/metal/cubes.vert"), // vertex shader
                TestLauncher.class.getResource("/shaders/metal/cubes.frag")  // fragment shader
        );


        long numFrames = 1L;
        long totalTime = 0L;
        long lastTime;
        long startTime = lastTime = glfwGetTimerValue();

        while (!glfwWindowShouldClose(window)) {
            glfwPollEvents();

            long now = glfwGetTimerValue();
            long frameTime = now - lastTime;
            lastTime = now;
            numFrames++;
            totalTime += frameTime;

            if (numFrames % 500 == 0) {
                numFrames = 1;
                totalTime = 0;
            }

            double freq = glfwGetTimerFrequency();
            double toMs = 1000.0 / freq;

            float time = (float)((now - startTime) / freq);

            bgfx_dbg_text_printf(0, 1, 0x4f, "bgfx/examples/01-cubes");
            bgfx_dbg_text_printf(0, 2, 0x6f, "Description: Rendering simple static mesh.");
            bgfx_dbg_text_printf(0, 3, 0x0f, String.format("Frame: %7.3f[ms]", (float)(frameTime * toMs)));

            // Set view 0 default viewport.
            bgfx_set_view_rect(0, 0, 0, width, height);

//            // This dummy draw call is here to make sure that view 0 is cleared
//            // if no other draw calls are submitted to view 0.
            bgfx_touch(0);

            // MY CODE

            lookAt(new Vector3f(0.0f, 0.0f, 0.0f), new Vector3f(0.0f, 0.0f, -35.0f), view);
            perspective(60.0f, width, height, 0.1f, 100.0f, proj);

            view.get(viewBuf);
            proj.get(projBuf);
            bgfx_set_view_transform(0, viewBuf, projBuf);

            long encoder = bgfx_encoder_begin(false);

            // this is for the model's offset
            for (int yy = 0; yy < 11; ++yy) {
                for (int xx = 0; xx < 11; ++xx) {

                    model
                            .identity()
                            .translate(
                                    -15.0f + xx * 3.0f,
                                    -15.0f + yy * 3.0f,
                                    0.0f)
                            .rotateAffineXYZ(
                                    time + xx * 0.21f,
                                    time + yy * 0.37f,
                                    0.0f)
                    ;

                    model.get(modelBuf);

                    bgfx_encoder_set_transform(encoder, modelBuf);
                    bgfx_encoder_set_vertex_buffer(encoder, 0, vertexBuffer.handle(), 0, 8, BGFX_INVALID_HANDLE);
                    bgfx_encoder_set_index_buffer(encoder, indexBuffer.handle(), 0, 36);
                    bgfx_encoder_set_state(encoder, BGFX_STATE_DEFAULT, 0);
                    bgfx_encoder_submit(encoder, 0, program.handle(), 0, false);
                }
            }

//            bgfx_encoder_set_transform(encoder, modelBuf);
//
//            bgfx_encoder_set_vertex_buffer(encoder, 0, vertices.handle(), 0, 8, BGFX_INVALID_HANDLE);
//
//            bgfx_encoder_set_index_buffer(encoder, indices.handle(), 0, 36);
//            bgfx_encoder_set_state(encoder, BGFX_STATE_DEFAULT, 0);
//            bgfx_encoder_submit(encoder, 0, program.handle(), 0, false);



//            // Use debug font to print information about this example.
//            bgfx_dbg_text_clear(0, false);
//            bgfx_dbg_text_image(Math.max(width / 2 / 8, 20) - 20,
//                    Math.max(height / 2 / 16, 6) - 6,
//                    40,
//                    12,
//                    logo,
//                    160
//            );
//
//            bgfx_dbg_text_printf(0, 1, 0x1f, "bgfx/examples/25-c99");
//            bgfx_dbg_text_printf(0, 2, 0x3f, "Description: Initialization and debug text with C99 API.");
//
//            bgfx_dbg_text_printf(0, 3, 0x0f, "Color can be changed with ANSI \u001b[9;me\u001b[10;ms\u001b[11;mc\u001b[12;ma\u001b[13;mp\u001b[14;me\u001b[0m code too.");
//
//            bgfx_dbg_text_printf(80, 4, 0x0f, "\u001b[;0m    \u001b[;1m    \u001b[; 2m    \u001b[; 3m    \u001b[; 4m    \u001b[; 5m    \u001b[; 6m    \u001b[; 7m    \u001b[0m");
//            bgfx_dbg_text_printf(80, 5, 0x0f, "\u001b[;8m    \u001b[;9m    \u001b[;10m    \u001b[;11m    \u001b[;12m    \u001b[;13m    \u001b[;14m    \u001b[;15m    \u001b[0m");
//
//            // Advance to next frame. Rendering thread will be kicked to
//            // process submitted rendering primitives.
            bgfx_frame(false);

            bgfx_encoder_end(encoder);
        }

        bgfx_shutdown();

//        glfwFreeCallbacks(window);
        glfwDestroyWindow(window);
        glfwTerminate();
    }

